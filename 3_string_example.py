# create a string using double or single quotes
firstName = "Kamrul"
lastName = "Hasan"
print(firstName)

print(firstName.upper())
print(firstName.lower())

# Format string
print("{}\t{}".format(firstName, lastName))


# Access String Characters in Python
print(f"First index the of {firstName} is: {firstName[0]}")
print(f"last index the of {firstName} is: {firstName[-1]}")

# Slicing
print(f"{firstName} name of 1st index to 4 index: {firstName[1:4]}")
